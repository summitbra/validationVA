package com.leroymerlin.va.validation.pojo;

public class OutgoingFlow
{
	private String code;
	private String name;

	// Getter Methods

	public String getCode( )
	{
		return code;
	}

	public String getName( )
	{
		return name;
	}

	// Setter Methods

	public void setCode( String code )
	{
		this.code = code;
	}

	public void setName( String name )
	{
		this.name = name;
	}
}