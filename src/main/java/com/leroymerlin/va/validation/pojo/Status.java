package com.leroymerlin.va.validation.pojo;

public class Status
{
	private String code;
	private String type;

	// Getter Methods

	public String getCode( )
	{
		return code;
	}

	public String getType( )
	{
		return type;
	}

	// Setter Methods

	public void setCode( String code )
	{
		this.code = code;
	}

	public void setType( String type )
	{
		this.type = type;
	}
}
